package com.gastonmartin.desafio.model;

public class AdditionRequest {
    private double left;
    private double right;

    public double getLeft() {
        return left;
    }

    public void setLeft(double left) {
        this.left = left;
    }

    public double getRight() {
        return right;
    }

    public void setRight(double right) {
        this.right = right;
    }
}
